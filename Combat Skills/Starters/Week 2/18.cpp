// Իրականացնել ֆունկցիա, որը ստանում է քառակուսային մատրից։ 
// Ֆունկցիան մատրիցի զույգ ինդեքսով տողերի էլեմենտները փոխարինում է 0-ներով և վերադարձնում։

void evens_to_zeros(int arr[5][5])
{
    for (int i = 0; i < 5; i += 2)
    {
        for (int j = 0; j < 5; ++j)
        {
            arr[i][j] = 0;
        }
    }
}