// անկյունագծից վերև գտնվող տարրերի գումարը անկյունագծի էլեմենտները ներառյալ։

int diagonal_uppers_sum(int arr[5][5])
{
    int sum {};

    for (int i = 0; i < 5; ++i)
    {
        for (int j = 0; j < 5 - i; ++j)
        {
            sum += arr[i][j];
        }
    }
    return sum;
}